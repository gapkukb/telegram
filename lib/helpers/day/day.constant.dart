/// ISO 8601：这是一种国际标准的日期和时间表示方式，格式为YYYY-MM-DDTHH:mm:ss.sssZ，其中 YYYY 代表四位数年份，MM 代表月份，DD 代表天数，T 作为日期和时间的分隔符，HH 代表小时，mm 代表分钟，ss.sss 代表秒和毫秒，Z 代表时区。例如：2023-04-13T15:30:00.000Z
final REGEXP_ISO_8601 = RegExp(r"^(\d{4})\-(\d{1,2})\-(\d{1,2})[Tt\s]*(\d{1,2})\:(\d{1,2})\:(\d{1,2})(?:\.(\d+))?([Zz]|([+-])(\d{1,2}):?(\d{1,2}))?$");

/// RFC 3339：这是一种在互联网上广泛使用的日期和时间表示方式，它基于 ISO 8601 格式，但对其进行了一些扩展，例如允许使用非零开头的小时、分钟和秒。例如：2023-04-13T15:30:00Z
final REGEXP_RFC_3339 = RegExp(r"^(\d{4})\-(\d{1,2})\-(\d{1,2})[Tt\s]*(\d{1,2})\:(\d{1,2})\:(\d{1,2})(?:\.(\d+))?([Zz]|([+-])(\d{1,2}):?(\d{1,2}))?$");

/// RFC 2822：这是一种用于电子邮件日期和时间表示的格式，例如：Thu, 13 Apr 2023 15:30:00 +0000
final REGEXP_RFC_2822 = RegExp(r"^(\d{4})\-(\d{1,2})\-(\d{1,2})[Tt\s]*(\d{1,2})\:(\d{1,2})\:(\d{1,2})(?:\.(\d+))?([Zz]|([+-])(\d{1,2}):?(\d{1,2}))?$");

/// Unix 时间戳：这是一种用于表示自 1970 年 1 月 1 日 00:00:00 UTC（世界协调时间）以来的秒数。例如：1678882200
final REGEXP_UNIX = RegExp(r"^(\d{4})\-(\d{1,2})\-(\d{1,2})[Tt\s]*(\d{1,2})\:(\d{1,2})\:(\d{1,2})(?:\.(\d+))?([Zz]|([+-])(\d{1,2}):?(\d{1,2}))?$");

/// 自然语言表示：这种格式以人类易读的方式表示日期和时间，例如：April 13, 2023, 3:30 PM
